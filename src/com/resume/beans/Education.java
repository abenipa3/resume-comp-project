package com.resume.beans;

public class Education {
	
	private String schoolAttended;
	private String degreeReceived;
	private String achievements;
	private String schoolCity;
	private String schoolState;
	
	public String getSchoolAttended() {
		return schoolAttended;
	}
	public String getDegreeReceived() {
		return degreeReceived;
	}
	public String getAchievements() {
		return achievements;
	}
	public String getSchoolCity() {
		return schoolCity;
	}
	public String getSchoolState() {
		return schoolState;
	}
	public void setSchoolAttended(String schoolAttended) {
		this.schoolAttended = schoolAttended;
	}
	public void setDegreeReceived(String degreeReceived) {
		this.degreeReceived = degreeReceived;
	}
	public void setAchievements(String achievements) {
		this.achievements = achievements;
	}
	public void setSchoolCity(String schoolCity) {
		this.schoolCity = schoolCity;
	}
	public void setSchoolState(String schoolState) {
		this.schoolState = schoolState;
	}
}
